    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        Motocicleta moto;
        moto = new Motocicleta();
        
        moto.setTitular(txtTitular.getText());
        moto.setModelo(txtModelo.getText());
        moto.setPatente(txtPatente.getText());
        moto.setMarca(txtMarca.getText());
        moto.setPrecio(Float.parseFloat(txtPrecio.getText()));
        moto.setAñoPatentamiento(Integer.parseInt(txtAñoPat.getText()));
        moto.setKm(Integer.parseInt(txtKilometraje.getText()));
        moto.setCantRuedas(Integer.parseInt(txtCantRuedas.getText()));
        moto.setCilindrada(Integer.parseInt(txtCilindrada.getText()));
        
        lblTipo.setText(moto.indicarTipo());
        lblCosto.setText(String.valueOf(moto.calcularCostoTransferencia()));
        lblAntiguedad.setText(String.valueOf(moto.calcularAntiguedad()));
        
    }//GEN-LAST:event_btnCalcularActionPerformed

