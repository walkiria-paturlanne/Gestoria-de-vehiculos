    private void txtAñoPatKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAñoPatKeyTyped
         char validar=evt.getKeyChar();
         
         if (Character.isLetter(validar)){
         getToolkit().beep();
         evt.consume();
         
         JOptionPane.showMessageDialog(rootPane,"Ingresar sólo números");
         
         }
    }//GEN-LAST:event_txtAñoPatKeyTyped

